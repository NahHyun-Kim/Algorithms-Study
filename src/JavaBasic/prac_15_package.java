package JavaBasic;

public class prac_15_package {

	public static void main(String[] args) {
		/*
		 * package란 : 윈도우 및 리눅스에서의 폴더, 디렉토리 개념
		 * 자바의 클래스 파일을 몰아놓은 폴더로, 주석문 제외 첫 행에 package명이 들어간다.
		 * package 이름은 소문자로 명명하는 것이 관례임.
		 * kr.ac.kopo.util 처럼 패키이 이름의 중복 방지를 위해 도메인 이름을 역순으로 사용
		 * 
		 * 다른 패키지에 있는 클래스를 선언해서 사용할 경우는 패키지 명 전체를 쓰는것이 아닌,
		 * import(c/s/o) 또는 class명을 입력 후 import를 하여 사용한다.
		 * 
		 * import는 import com.hankuk.*; 과 같이 hankuk 안에 있는 모든 클래스를 import 해준다.
		 * 이 import를 해줌으로서, 컴파일러에 클래스의 경로를 알려준다. 따라서 new();를 통한 객체 생성시 패키지명을 따로 써줄 필요가 없다.
		 * (이미 임포트가 되었기 때문)
		 * 
		 * 
		 * 자바에서 제공하는 기본 및 확장 패키지들
		 * java.awt; 그래픽 처리 API (Abstract Windows Toolkit)
		 * java.io; 입출력을 스트림하는 API (Input / Output)
		 * java.lang; 자바 프로그램의 필수 API
		 * java.math; 수학과 관련된 API
		 * java.net; 네트워크를 처리하는 API
		 * java.text; 날짜, 시간, 문자열 등 지역화를 처리하는 API
		 * java.time; JDK 8이 지원하는 날짜 및 시간을 처리하는 API
		 * java.util; 날짜, 리스트, 벡터 등 유틸리티 API
		 * Util(utility)이란  ? 프로그래밍에 유용한 클래스들을 모아둔 것
		 * javax.swing; 스윙 컴포넌트 API
		 * Swing? 모든 운영체제에서 동일한 느낌을 주며, 자바 영역에서 사용하는 look&feel을 적용받아 사용. JButton...
		 * */


		
	}

}
