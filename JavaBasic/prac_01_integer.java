 package JavaBasic;

public class prac_01_integer {

	public static void main(String[] args) {
		// 정수 데이터 타입 practice
		int fifteen = 15; //10진수 15
		byte fifteen2 = 0b1111; //2진수 15
		short fifteen3 = 017; //8진수 15
		int fifteen4 = 0xF; //16진수 15
		long lightSpeed = 30000L; //L로 long 타입임을 명시
		
		double half = 0.5;
		double half2 = 5E-1; //지수 표기법으로 5*10-1을 의미(=0.5)
		float pi = 3.14159F; //F로 float 타입임을 명시
		double pi2 = 3.14159; //double 타입은 받을 수 있다.
		
	}

}
